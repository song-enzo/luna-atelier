def seed():
    with app.app_context():
        db.create_all()
        
        # Check admin
        admin = User.query.filter_by(username='admin').first()
        if not admin:
            admin = User(username='admin', nickname='管理员', role='admin')
            admin.set_password('admin123')
            db.session.add(admin)
        
        # Check if already seeded
        existing = Style.query.count()
        if existing >= 42:
            print(f"已有 {existing} 个款式，跳过导入")
            return
        
        # Clear old styles
        Style.query.delete()
        
        for i, (name, code, desc, fabric, image, price) in enumerate(STYLES, 1):
            s = Style(
                name=name, code=str(code), description=desc,
                fabric_info=fabric, image_path=f"styles/{image}", price=price
            )
            db.session.add(s)
        
        db.session.commit()
        print(f"✅ 已导入 {len(STYLES)} 个款式")

if __name__ == '__main__':
    seed()
    print("完成")
